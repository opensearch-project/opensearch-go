// pluginClientOp holds the info needed to generate one dispatch method.
type pluginClientOp struct {
	MethodName   string
	TypePrefix   string
	IsPointerReq bool
	IsNoBody     bool
}

var pluginClientTmpl = template.Must(template.New("plugin_client").Parse(`// Code generated by cmd/osgen; DO NOT EDIT.

package {{.Pkg}}

import (
	"context"
	"fmt"

	"github.com/opensearch-project/opensearch-go/v4"
)

// Client provides methods for the {{.Pkg}} plugin API.
type Client struct {
	Client *opensearch.Client
}

// NewClient creates a new {{.Pkg}} plugin client wrapping the given opensearch.Client.
func NewClient(client *opensearch.Client) *Client {
	return &Client{Client: client}
}

// do calls [opensearch.Do] and checks the response for errors.
func do[T any](ctx context.Context, c *Client, req opensearch.Request, dataPointer *T) (*opensearch.Response, error) {
	resp, err := opensearch.Do(ctx, c.Client, req, dataPointer)
	if err != nil {
		return nil, err
	}

	if resp.IsError() {
		if dataPointer != nil {
			return resp, opensearch.ParseError(resp)
		}
		return resp, fmt.Errorf("status: %s", resp.Status())
	}

	return resp, nil
}
{{range .Ops}}
{{- if .IsPointerReq}}
// {{.MethodName}} executes the {{.TypePrefix}} operation.
func (c *Client) {{.MethodName}}(ctx context.Context, req *{{.TypePrefix}}Req) ({{if .IsNoBody}}*opensearch.Response{{else}}*{{.TypePrefix}}Resp{{end}}, error) {
	if req == nil {
		req = &{{.TypePrefix}}Req{}
	}
{{- if .IsNoBody}}
	return do(ctx, c, *req, noBody)
{{- else}}
	var resp {{.TypePrefix}}Resp
	if _, err := do(ctx, c, *req, &resp); err != nil {
		return &resp, err
	}
	return &resp, nil
{{- end}}
}
{{- else}}
// {{.MethodName}} executes the {{.TypePrefix}} operation.
func (c *Client) {{.MethodName}}(ctx context.Context, req {{.TypePrefix}}Req) ({{if .IsNoBody}}*opensearch.Response{{else}}*{{.TypePrefix}}Resp{{end}}, error) {
{{- if .IsNoBody}}
	return do(ctx, c, req, noBody)
{{- else}}
	var resp {{.TypePrefix}}Resp
	if _, err := do(ctx, c, req, &resp); err != nil {
		return &resp, err
	}
	return &resp, nil
{{- end}}
}
{{- end}}
{{end}}
// noBody is a sentinel used when an operation returns no JSON body.
var noBody *struct{} //nolint:gochecknoglobals // package-level marker shared by all no-body operations
`))

// renderPluginClientFile generates the client_gen.go for a plugin package.
func renderPluginClientFile(pkg string, ops []apiOperation) (string, error) {
	var clientOps []pluginClientOp
	for _, op := range ops {
		suffix := op.Group
		if idx := strings.IndexByte(suffix, '.'); idx >= 0 {
			suffix = suffix[idx+1:]
		}
		clientOps = append(clientOps, pluginClientOp{
			MethodName:   methodNameFromSuffix(suffix),
			TypePrefix:   op.TypePrefix,
			IsPointerReq: op.IsPointerReq,
			IsNoBody:     op.IsNoBody,
		})
	}

	data := struct {
		Pkg string
		Ops []pluginClientOp
	}{Pkg: pkg, Ops: clientOps}

	var sb strings.Builder
	if err := pluginClientTmpl.Execute(&sb, data); err != nil {
		return "", fmt.Errorf("executing plugin client template for %s: %w", pkg, err)
	}

	formatted, err := format.Source([]byte(sb.String()))
	if err != nil {
		return "", fmt.Errorf("formatting plugin client for %s: %w\n%s", pkg, err, sb.String())
	}
	return string(formatted), nil
}

var pluginTestHelperTmpl = template.Must(template.New("plugin_test_helper").Parse(`// Code generated by cmd/osgen; DO NOT EDIT.

package {{.TestPkg}}

import (
	"net/http"
	"net/http/httptest"
	"testing"

	"github.com/opensearch-project/opensearch-go/v4"

	"{{.PluginImport}}"
	"{{.CoreImport}}/testutil"
)

// NewClient returns a plugin client connected to the integration test cluster.
func NewClient(t *testing.T) (*{{.Pkg}}.Client, error) {
	t.Helper()
	config := testutil.ClientConfig(t)
	osClient, err := opensearch.NewClient(config.Client)
	if err != nil {
		return nil, err
	}
	return {{.Pkg}}.NewClient(osClient), nil
}

// CreateFailingClient returns a plugin client that always fails with a transport error.
func CreateFailingClient(t *testing.T) (*{{.Pkg}}.Client, error) {
	t.Helper()
	ts := httptest.NewServer(http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		// Close immediately to cause a transport error.
		hj, ok := w.(http.Hijacker)
		if !ok {
			t.Fatal("server does not support hijacking")
		}
		conn, _, _ := hj.Hijack()
		conn.Close()
	}))
	t.Cleanup(ts.Close)

	osClient, err := opensearch.NewClient(opensearch.Config{
		Addresses: []string{ts.URL},
	})
	if err != nil {
		return nil, err
	}
	return {{.Pkg}}.NewClient(osClient), nil
}
`))

// renderPluginTestHelper generates internal/test/helpers_gen.go for a plugin.
func renderPluginTestHelper(pkg, pluginImport, coreImport string) (string, error) {
	// Test package name is the conventional "<prefix>test" format.
	testPkg := pkg + "test"

	data := struct {
		TestPkg      string
		Pkg          string
		PluginImport string
		CoreImport   string
	}{
		TestPkg:      testPkg,
		Pkg:          pkg,
		PluginImport: pluginImport,
		CoreImport:   coreImport,
	}

	var sb strings.Builder
	if err := pluginTestHelperTmpl.Execute(&sb, data); err != nil {
		return "", fmt.Errorf("executing plugin test helper template for %s: %w", pkg, err)
	}

	formatted, err := format.Source([]byte(sb.String()))
	if err != nil {
		return "", fmt.Errorf("formatting plugin test helper for %s: %w\n%s", pkg, err, sb.String())
	}
	return string(formatted), nil
}
