type testCase struct {
	Name     string
	Fields   string
	WantPath string
	WantErr  string
}

type testBuilder struct {
	StructName string
	TestCases  []testCase
}

func generateTests(builders []builder, pkg string) (string, error) {
	var testBuilders []testBuilder

	for _, b := range builders {
		tb := testBuilder{StructName: b.StructName}
		tb.TestCases = synthesizeTestCases(b)
		testBuilders = append(testBuilders, tb)
	}

	var sb strings.Builder
	if err := testTmpl.Execute(&sb, struct {
		Pkg      string
		Builders []testBuilder
	}{
		Pkg:      pkg,
		Builders: testBuilders,
	}); err != nil {
		return "", err
	}

	formatted, err := format.Source([]byte(sb.String()))
	if err != nil {
		lineNum := 0
		fmt.Sscanf(err.Error(), "%d:", &lineNum)
		if lineNum > 0 {
			return "", fmt.Errorf("formatting tests: %w\n%s", err, extractLines(sb.String(), lineNum-5, lineNum+10))
		}
		return "", fmt.Errorf("formatting tests: %w", err)
	}
	return string(formatted), nil
}

func synthesizeTestCases(b builder) []testCase {
	var cases []testCase

	var requiredFields []builderField
	for _, f := range b.Fields {
		if f.Required {
			requiredFields = append(requiredFields, f)
		}
	}
	hasRequired := len(requiredFields) > 0

	emptyFields := b.StructName + "{}"
	if hasRequired {
		cases = append(cases, testCase{
			Name:     "required fields empty",
			Fields:   emptyFields,
			WantPath: "",
			WantErr:  "true",
		})
	} else {
		path := simulateBuild(b, nil)
		cases = append(cases, testCase{
			Name:     "all empty",
			Fields:   emptyFields,
			WantPath: path,
			WantErr:  "false",
		})
	}

	// Builders with 2+ required fields: emit a "missing <Field>" case per
	// required field where every other required field is set. Each case
	// must reject with errRequired so partial-population bugs are caught.
	if len(requiredFields) > 1 {
		for i, missing := range requiredFields {
			partial := make(map[string][]string, len(requiredFields)-1)
			for j, rf := range requiredFields {
				if i == j {
					continue
				}
				partial[rf.Name] = []string{"req-" + strings.ToLower(rf.Name)}
			}
			cases = append(cases, testCase{
				Name:     "missing " + missing.Name,
				Fields:   buildStructLiteral(b, partial),
				WantPath: "",
				WantErr:  "true",
			})
		}
	}

	values := make(map[string][]string)
	for _, f := range b.Fields {
		values[f.Name] = []string{"test-" + strings.ToLower(f.Name)}
	}
	if len(b.Fields) > 0 {
		path := simulateBuild(b, values)
		cases = append(cases, testCase{
			Name:     "all fields",
			Fields:   buildStructLiteral(b, values),
			WantPath: path,
			WantErr:  "false",
		})
	}

	if !hasRequired && len(b.Fields) > 1 {
		for _, f := range b.Fields {
			singleValue := map[string][]string{
				f.Name: {"val-" + strings.ToLower(f.Name)},
			}
			if violatesPositional(b, singleValue) {
				cases = append(cases, testCase{
					Name:     "only " + f.Name,
					Fields:   buildStructLiteral(b, singleValue),
					WantPath: "",
					WantErr:  "true",
				})
				continue
			}
			path := simulateBuild(b, singleValue)
			cases = append(cases, testCase{
				Name:     "only " + f.Name,
				Fields:   buildStructLiteral(b, singleValue),
				WantPath: path,
				WantErr:  "false",
			})
		}
	}

	// Multi-value cases: one per list field, not just the first. Each case
	// holds every required field steady and replaces the list field with a
	// 3-element slice so writeSegments() join behavior is exercised.
	for _, f := range b.Fields {
		if !f.IsList {
			continue
		}
		multiValue := make(map[string][]string)
		for _, rf := range b.Fields {
			if rf.Required {
				multiValue[rf.Name] = []string{"req-" + strings.ToLower(rf.Name)}
			}
		}
		multiValue[f.Name] = []string{"a", "b", "c"}
		if violatesPositional(b, multiValue) {
			cases = append(cases, testCase{
				Name:     f.Name + " multi-value",
				Fields:   buildStructLiteral(b, multiValue),
				WantPath: "",
				WantErr:  "true",
			})
			continue
		}
		path := simulateBuild(b, multiValue)
		cases = append(cases, testCase{
			Name:     f.Name + " multi-value",
			Fields:   buildStructLiteral(b, multiValue),
			WantPath: path,
			WantErr:  "false",
		})
	}

	return cases
}

// violatesPositional reports whether the supplied field values trip any
// positional-dependency guard declared on the builder. Returns true when a
// dependent field is set and its predecessor is not, mirroring the runtime
// check emitted at the top of Build().
func violatesPositional(b builder, values map[string][]string) bool {
	for _, dep := range b.PositionalDeps {
		depVals := values[dep.Dependent.Name]
		predVals := values[dep.Predecessor.Name]
		if hasNonEmptyVals(depVals) && !hasNonEmptyVals(predVals) {
			return true
		}
	}
	return false
}

func hasNonEmptyVals(vs []string) bool {
	for _, v := range vs {
		if v != "" {
			return true
		}
	}
	return false
}

// simulateBuild returns the path that Build() would produce for the given field values.
func simulateBuild(b builder, values map[string][]string) string {
	if len(b.Ops) == 0 {
		return "/"
	}

	var result strings.Builder
	// Frame tracks the writing state of one nested switch{} or if{}
	// block. `active` is true iff the surrounding case/branch should
	// emit segments. `taken` (switches only) records whether some prior
	// case has already matched, so subsequent cases (and default) are
	// disabled.
	type frame struct {
		isSwitch bool
		active   bool
		taken    bool
	}
	var stack []frame

	isActive := func() bool {
		for i := len(stack) - 1; i >= 0; i-- {
			if !stack[i].active {
				return false
			}
		}
		return true
	}

	condsAllMatch := func(conds []caseCondition) bool {
		for _, c := range conds {
			vals := values[c.Field]
			if c.IsList {
				if len(vals) == 0 {
					return false
				}
				continue
			}
			if len(vals) == 0 || vals[0] == "" {
				return false
			}
		}
		return len(conds) > 0
	}

	for _, op := range b.Ops {
		switch op.Kind {
		case opLit:
			if isActive() {
				result.WriteString("/")
				result.WriteString(op.Value)
			}
		case opField:
			if isActive() {
				vals := values[op.Value]
				if len(vals) > 0 && vals[0] != "" {
					result.WriteString("/")
					result.WriteString(vals[0])
				}
			}
		case opList:
			if isActive() {
				vals := values[op.Value]
				if len(vals) > 0 {
					result.WriteString("/")
					result.WriteString(strings.Join(vals, ","))
				}
			}
		case opSwitch:
			stack = append(stack, frame{isSwitch: true, active: false, taken: false})
		case opCase:
			if len(stack) > 0 && stack[len(stack)-1].isSwitch {
				top := &stack[len(stack)-1]
				if top.taken {
					top.active = false
				} else if condsAllMatch(op.Conditions) {
					top.active = true
					top.taken = true
				} else {
					top.active = false
				}
			}
		case opDefault:
			if len(stack) > 0 && stack[len(stack)-1].isSwitch {
				top := &stack[len(stack)-1]
				top.active = !top.taken
				top.taken = true
			}
		case opSwitchEnd:
			if len(stack) > 0 {
				stack = stack[:len(stack)-1]
			}
		case opIf:
			active := condsAllMatch(op.Conditions)
			stack = append(stack, frame{active: active})
		case opIfEnd:
			if len(stack) > 0 {
				stack = stack[:len(stack)-1]
			}
		case opExplainCheck:
			// Runtime-only invalid-combination guard; never fires for
			// the deterministic test fixtures we drive into Build().
		}
	}
	path := result.String()
	if path == "" {
		path = "/"
	}
	return path
}

func buildStructLiteral(b builder, values map[string][]string) string {
	var sb strings.Builder
	sb.WriteString(b.StructName)
	sb.WriteString("{")
	first := true
	for _, f := range b.Fields {
		vals, ok := values[f.Name]
		if !ok || len(vals) == 0 {
			continue
		}
		if !first {
			sb.WriteString(", ")
		}
		sb.WriteString(f.Name)
		sb.WriteString(": ")
		if f.IsList {
			sb.WriteString("[]string{")
			for i, v := range vals {
				if i > 0 {
					sb.WriteString(", ")
				}
				fmt.Fprintf(&sb, "%q", v)
			}
			sb.WriteString("}")
		} else {
			fmt.Fprintf(&sb, "%q", vals[0])
		}
		first = false
	}
	sb.WriteString("}")
	return sb.String()
}
