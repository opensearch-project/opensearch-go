package main

import (
	"fmt"
	"go/format"
	"strings"
	"text/template"
)

var testTmpl = template.Must(template.New("tests").Funcs(template.FuncMap{
	"quote": func(s string) string { return fmt.Sprintf("%q", s) },
}).Parse(`// Code generated by cmd/osgen; DO NOT EDIT.

package {{.Pkg}}

import (
	"testing"

	"github.com/stretchr/testify/require"
)
{{range .Builders}}
func Test{{.StructName}}_Build(t *testing.T) {
	t.Parallel()
	tests := []struct {
		name     string
		path     {{.StructName}}
		wantPath string
		wantErr  bool
	}{
{{- range .TestCases}}
		{name: {{quote .Name}}, path: {{.Fields}}, wantPath: {{quote .WantPath}}, wantErr: {{.WantErr}}},
{{- end}}
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			t.Parallel()
			gotPath, err := tt.path.Build()
			if tt.wantErr {
				require.Error(t, err)
				return
			}
			require.NoError(t, err)
			require.Equal(t, tt.wantPath, gotPath)
		})
	}
}
{{end}}`))

type testCase struct {
	Name     string
	Fields   string
	WantPath string
	WantErr  string
}

type testBuilder struct {
	StructName string
	TestCases  []testCase
}

func generateTests(builders []builder, pkg string) (string, error) {
	var testBuilders []testBuilder

	for _, b := range builders {
		tb := testBuilder{StructName: b.StructName}
		tb.TestCases = synthesizeTestCases(b)
		testBuilders = append(testBuilders, tb)
	}

	var sb strings.Builder
	if err := testTmpl.Execute(&sb, struct {
		Pkg      string
		Builders []testBuilder
	}{
		Pkg:      pkg,
		Builders: testBuilders,
	}); err != nil {
		return "", err
	}

	formatted, err := format.Source([]byte(sb.String()))
	if err != nil {
		lineNum := 0
		fmt.Sscanf(err.Error(), "%d:", &lineNum)
		if lineNum > 0 {
			return "", fmt.Errorf("formatting tests: %w\n%s", err, extractLines(sb.String(), lineNum-5, lineNum+10))
		}
		return "", fmt.Errorf("formatting tests: %w", err)
	}
	return string(formatted), nil
}

func synthesizeTestCases(b builder) []testCase {
	var cases []testCase

	hasRequired := false
	for _, f := range b.Fields {
		if f.Required {
			hasRequired = true
			break
		}
	}

	emptyFields := b.StructName + "{}"
	if hasRequired {
		cases = append(cases, testCase{
			Name:     "required fields empty",
			Fields:   emptyFields,
			WantPath: "",
			WantErr:  "true",
		})
	} else {
		path := simulateBuild(b, nil)
		cases = append(cases, testCase{
			Name:     "all empty",
			Fields:   emptyFields,
			WantPath: path,
			WantErr:  "false",
		})
	}

	values := make(map[string][]string)
	for _, f := range b.Fields {
		values[f.Name] = []string{"test-" + strings.ToLower(f.Name)}
	}
	if len(b.Fields) > 0 {
		path := simulateBuild(b, values)
		cases = append(cases, testCase{
			Name:     "all fields",
			Fields:   buildStructLiteral(b, values),
			WantPath: path,
			WantErr:  "false",
		})
	}

	if !hasRequired && len(b.Fields) > 1 {
		for _, f := range b.Fields {
			singleValue := map[string][]string{
				f.Name: {"val-" + strings.ToLower(f.Name)},
			}
			path := simulateBuild(b, singleValue)
			cases = append(cases, testCase{
				Name:     "only " + f.Name,
				Fields:   buildStructLiteral(b, singleValue),
				WantPath: path,
				WantErr:  "false",
			})
		}
	}

	for _, f := range b.Fields {
		if f.IsList {
			multiValue := make(map[string][]string)
			for _, rf := range b.Fields {
				if rf.Required {
					multiValue[rf.Name] = []string{"req-" + strings.ToLower(rf.Name)}
				}
			}
			multiValue[f.Name] = []string{"a", "b", "c"}
			path := simulateBuild(b, multiValue)
			cases = append(cases, testCase{
				Name:     f.Name + " multi-value",
				Fields:   buildStructLiteral(b, multiValue),
				WantPath: path,
				WantErr:  "false",
			})
			break
		}
	}

	return cases
}

// simulateBuild returns the path that Build() would produce for the given field values.
func simulateBuild(b builder, values map[string][]string) string {
	if len(b.Ops) == 0 {
		return "/"
	}

	var result strings.Builder
	type frame struct {
		active bool
	}
	var stack []frame

	isActive := func() bool {
		if len(stack) == 0 {
			return true
		}
		return stack[len(stack)-1].active
	}

	for _, op := range b.Ops {
		switch op.Kind {
		case opLit:
			if isActive() {
				result.WriteString("/")
				result.WriteString(op.Value)
			}
		case opField:
			if isActive() {
				vals := values[op.Value]
				if len(vals) > 0 && vals[0] != "" {
					result.WriteString("/")
					result.WriteString(vals[0])
				}
			}
		case opList:
			if isActive() {
				vals := values[op.Value]
				if len(vals) > 0 {
					result.WriteString("/")
					result.WriteString(strings.Join(vals, ","))
				}
			}
		case opIfList:
			vals := values[op.Value]
			active := len(vals) > 0
			if !isActive() {
				active = false
			}
			stack = append(stack, frame{active: active})
		case opIfStr:
			vals := values[op.Value]
			active := len(vals) > 0 && vals[0] != ""
			if !isActive() {
				active = false
			}
			stack = append(stack, frame{active: active})
		case opElseIfList:
			if len(stack) > 0 {
				prev := stack[len(stack)-1]
				stack = stack[:len(stack)-1]
				vals := values[op.Value]
				active := !prev.active && len(vals) > 0
				if !isActive() {
					active = false
				}
				stack = append(stack, frame{active: active})
			}
		case opElseIfStr:
			if len(stack) > 0 {
				prev := stack[len(stack)-1]
				stack = stack[:len(stack)-1]
				vals := values[op.Value]
				active := !prev.active && len(vals) > 0 && vals[0] != ""
				if !isActive() {
					active = false
				}
				stack = append(stack, frame{active: active})
			}
		case opElse:
			if len(stack) > 0 {
				prev := stack[len(stack)-1]
				stack = stack[:len(stack)-1]
				active := !prev.active
				if !isActive() {
					active = false
				}
				stack = append(stack, frame{active: active})
			}
		case opEnd:
			if len(stack) > 0 {
				stack = stack[:len(stack)-1]
			}
		}
	}
	path := result.String()
	if path == "" {
		path = "/"
	}
	return path
}

func buildStructLiteral(b builder, values map[string][]string) string {
	var sb strings.Builder
	sb.WriteString(b.StructName)
	sb.WriteString("{")
	first := true
	for _, f := range b.Fields {
		vals, ok := values[f.Name]
		if !ok || len(vals) == 0 {
			continue
		}
		if !first {
			sb.WriteString(", ")
		}
		sb.WriteString(f.Name)
		sb.WriteString(": ")
		if f.IsList {
			sb.WriteString("[]string{")
			for i, v := range vals {
				if i > 0 {
					sb.WriteString(", ")
				}
				fmt.Fprintf(&sb, "%q", v)
			}
			sb.WriteString("}")
		} else {
			fmt.Fprintf(&sb, "%q", vals[0])
		}
		first = false
	}
	sb.WriteString("}")
	return sb.String()
}
