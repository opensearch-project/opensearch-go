// paramTestCase holds data for one row in the params test table.
type paramTestCase struct {
	Name        string
	FieldAssign string
	WantAssign  string
}

// reqTestCase holds data for one row in the GetRequest test table.
type reqTestCase struct {
	Name        string
	FieldAssign string
	WantMethod  string
	WantPath    string
	WantErr     string
}

// dispatchEntry holds data for one dispatch signature test function.
type dispatchEntry struct {
	TestName   string
	FieldPath  string // e.g. "" for top-level, "Cluster" for sub-client, "Indices.Alias" for nested
	MethodName string
	ReqType    string
	RespType   string
}

// renderParamsTest generates the _internal_gen_test.go content for an operation.
func renderParamsTest(op apiOperation, pkg string) (string, error) {
	cases := synthesizeParamCases(op)
	if len(op.QueryParams) == 0 && len(cases) == 0 {
		return "", nil
	}

	hasDuration := false
	for _, p := range op.QueryParams {
		if p.IsDuration {
			hasDuration = true
			break
		}
	}

	data := struct {
		Pkg         string
		TypePrefix  string
		HasDuration bool
		Cases       []paramTestCase
	}{
		Pkg:         pkg,
		TypePrefix:  op.TypePrefix,
		HasDuration: hasDuration,
		Cases:       cases,
	}

	var sb strings.Builder
	if err := paramsTestTmpl.Execute(&sb, data); err != nil {
		return "", fmt.Errorf("executing params test template for %s: %w", op.Group, err)
	}

	formatted, err := format.Source([]byte(sb.String()))
	if err != nil {
		return "", fmt.Errorf("formatting params test for %s: %w", op.Group, err)
	}
	return string(formatted), nil
}

// renderReqTest generates the _gen_test.go content (black-box) for an operation.
func renderReqTest(op apiOperation, pkg, importPkg string) (string, error) {
	cases := synthesizeReqCases(op)
	if len(cases) == 0 {
		return "", nil
	}

	needsStrings := false
	for _, c := range cases {
		if strings.Contains(c.FieldAssign, "strings.NewReader") {
			needsStrings = true
			break
		}
	}

	data := struct {
		Pkg          string
		PkgName      string
		ImportPath   string
		TypePrefix   string
		NeedsStrings bool
		Cases        []reqTestCase
	}{
		Pkg:          pkg,
		PkgName:      pkg,
		ImportPath:   importPkg,
		TypePrefix:   op.TypePrefix,
		NeedsStrings: needsStrings,
		Cases:        cases,
	}

	var sb strings.Builder
	if err := reqTestTmpl.Execute(&sb, data); err != nil {
		return "", fmt.Errorf("executing req test template for %s: %w", op.Group, err)
	}

	formatted, err := format.Source([]byte(sb.String()))
	if err != nil {
		return "", fmt.Errorf("formatting req test for %s: %w", op.Group, err)
	}
	return string(formatted), nil
}

// renderDispatchTest generates a single dispatch_gen_test.go for all operations.
func renderDispatchTest(ops []apiOperation, pkg, importPkg string) (string, error) {
	var entries []dispatchEntry
	for _, op := range ops {
		for _, route := range op.DispatchRoutes {
			if route.Deprecated {
				continue
			}
			entry := dispatchEntry{
				TestName:   op.TypePrefix,
				MethodName: route.MethodName,
			}

			// Build field path to reach the dispatch method from Client.
			if route.TopLevel {
				entry.FieldPath = ""
			} else {
				entry.FieldPath = resolveFieldPath(route.ReceiverType)
			}

			// Req type.
			if op.IsPointerReq {
				entry.ReqType = fmt.Sprintf("*%s.%sReq", pkg, op.TypePrefix)
			} else {
				entry.ReqType = fmt.Sprintf("%s.%sReq", pkg, op.TypePrefix)
			}

			// Resp type.
			if op.IsNoBody {
				entry.RespType = "*opensearch.Response"
			} else {
				entry.RespType = fmt.Sprintf("*%s.%sResp", pkg, op.TypePrefix)
			}

			entries = append(entries, entry)
		}
	}

	if len(entries) == 0 {
		return "", nil
	}

	data := struct {
		Pkg        string
		ImportPath string
		Entries    []dispatchEntry
	}{
		Pkg:        pkg,
		ImportPath: importPkg,
		Entries:    entries,
	}

	var sb strings.Builder
	if err := dispatchTestTmpl.Execute(&sb, data); err != nil {
		return "", fmt.Errorf("executing dispatch test template: %w", err)
	}

	formatted, err := format.Source([]byte(sb.String()))
	if err != nil {
		return "", fmt.Errorf("formatting dispatch test: %w\n%s", err, sb.String())
	}
	return string(formatted), nil
}

// resolveFieldPath converts a receiver type name to the field path used to
// access it from a *Client. e.g. "clusterClient" -> "Cluster",
// "aliasClient" -> "Indices.Alias".
func resolveFieldPath(receiverType string) string {
	for _, sc := range subClientHierarchy {
		if sc.TypeName == receiverType {
			if sc.Parent == "Client" {
				return sc.FieldName
			}
			return resolveFieldPath(sc.Parent) + "." + sc.FieldName
		}
	}
	return receiverType
}

// synthesizeParamCases produces test cases for each query parameter.
func synthesizeParamCases(op apiOperation) []paramTestCase {
	var cases []paramTestCase
	for _, p := range op.QueryParams {
		tc := paramTestCase{Name: p.ParamName}
		tc.FieldAssign, tc.WantAssign = paramTestValues(p)
		cases = append(cases, tc)
	}
	return cases
}

// paramTestValues returns the Go assignment for the field and the expected
// map entry for a given parameter type.
func paramTestValues(p apiQueryParam) (fieldAssign, wantAssign string) {
	switch {
	case p.IsDuration:
		fieldAssign = fmt.Sprintf("%s: 5 * time.Second", p.GoName)
		wantAssign = fmt.Sprintf("%q: %q", p.ParamName, "5000ms")
	case p.IsBool:
		fieldAssign = fmt.Sprintf("%s: true", p.GoName)
		wantAssign = fmt.Sprintf("%q: %q", p.ParamName, "true")
	case p.IsList:
		fieldAssign = fmt.Sprintf(`%s: []string{"a", "b"}`, p.GoName)
		wantAssign = fmt.Sprintf("%q: %q", p.ParamName, "a,b")
	case p.IsInt:
		fieldAssign = fmt.Sprintf("%s: 42", p.GoName)
		wantAssign = fmt.Sprintf("%q: %q", p.ParamName, "42")
	default:
		fieldAssign = fmt.Sprintf("%s: %q", p.GoName, "test-value")
		wantAssign = fmt.Sprintf("%q: %q", p.ParamName, "test-value")
	}
	return fieldAssign, wantAssign
}

// synthesizeReqCases produces test cases for GetRequest().
func synthesizeReqCases(op apiOperation) []reqTestCase {
	var cases []reqTestCase
	primaryMethod := http.MethodGet
	if len(op.HTTPMethods) > 0 {
		primaryMethod = op.HTTPMethods[0]
	}

	hasRequiredPath := false
	for _, f := range op.PathBuilder.Fields {
		if f.Required {
			hasRequiredPath = true
			break
		}
	}

	if !hasRequiredPath {
		basePath := evalPath(op, nil)
		cases = append(cases, reqTestCase{
			Name:       "empty request",
			WantMethod: primaryMethod,
			WantPath:   basePath,
			WantErr:    "false",
		})
	} else {
		cases = append(cases, reqTestCase{
			Name:    "missing required fields",
			WantErr: "true",
		})
	}

	if len(op.PathFields) > 0 {
		values := make(map[string][]string)
		var assigns []string
		for _, f := range op.PathFields {
			if f.IsList {
				values[f.GoName] = []string{"a", "b"}
				assigns = append(assigns, fmt.Sprintf(`%s: []string{"a", "b"}`, f.GoName))
			} else {
				values[f.GoName] = []string{"test-" + strings.ToLower(f.GoName)}
				assigns = append(assigns, fmt.Sprintf(`%s: %q`, f.GoName, "test-"+strings.ToLower(f.GoName)))
			}
		}
		fullPath := evalPath(op, values)
		cases = append(cases, reqTestCase{
			Name:        "all path fields",
			FieldAssign: strings.Join(assigns, ", "),
			WantMethod:  primaryMethod,
			WantPath:    fullPath,
			WantErr:     "false",
		})
	}

	if op.HasBody && len(op.HTTPMethods) > 1 && primaryMethod == http.MethodGet {
		values := make(map[string][]string)
		var assigns []string
		for _, f := range op.PathFields {
			if f.IsList {
				values[f.GoName] = []string{"x"}
				assigns = append(assigns, fmt.Sprintf(`%s: []string{"x"}`, f.GoName))
			} else {
				values[f.GoName] = []string{"test"}
				assigns = append(assigns, fmt.Sprintf(`%s: %q`, f.GoName, "test"))
			}
		}
		assigns = append(assigns, `Body: strings.NewReader("{}")`)
		cases = append(cases, reqTestCase{
			Name:        "body triggers POST",
			FieldAssign: strings.Join(assigns, ", "),
			WantMethod:  http.MethodPost,
			WantPath:    evalPath(op, values),
			WantErr:     "false",
		})
	}

	return cases
}

// evalPath evaluates the path builder trie with the given field values,
// producing the expected URL path for test assertions.
func evalPath(op apiOperation, values map[string][]string) string {
	path := simulateBuild(op.PathBuilder, values)
	if path == "" {
		return "/"
	}
	return path
}
