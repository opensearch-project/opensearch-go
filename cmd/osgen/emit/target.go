package emit

import (
	"fmt"
	"go/format"
	"strings"
)

// Fragment is a self-contained unit of generated code within a file.
// Each fragment knows what imports it needs and how to render its body.
type Fragment interface {
	// Imports returns the imports this fragment requires.
	Imports() []Import

	// Body renders the fragment's Go source (without package/import header).
	Body() (string, error)
}

// Target is a complete output file assembled from fragments.
type Target interface {
	// Path returns the absolute path of the output file.
	Path() string

	// Render assembles all fragments into a complete, formatted Go source file.
	Render() ([]byte, error)
}

// File is the standard Target implementation that assembles fragments.
type File struct {
	FilePath   string
	Package    string
	BuildTag   string // e.g. "!integration" or "integration"
	Fragments  []Fragment
}

func (f *File) Path() string { return f.FilePath }

func (f *File) Render() ([]byte, error) {
	// Collect imports from all fragments.
	var imps importSet
	for _, frag := range f.Fragments {
		for _, imp := range frag.Imports() {
			if imp.Alias != "" {
				imps.AddAlias(imp.Path, imp.Alias)
			} else {
				imps.Add(imp.Path)
			}
		}
	}

	// Render each fragment body.
	var bodies []string
	for _, frag := range f.Fragments {
		body, err := frag.Body()
		if err != nil {
			return nil, fmt.Errorf("rendering fragment in %s: %w", f.FilePath, err)
		}
		if body != "" {
			bodies = append(bodies, body)
		}
	}

	// Assemble the file.
	var sb strings.Builder
	sb.WriteString("// SPDX-License-Identifier: Apache-2.0\n")
	sb.WriteString("//\n")
	sb.WriteString("// The OpenSearch Contributors require contributions made to\n")
	sb.WriteString("// this file be licensed under the Apache-2.0 license or a\n")
	sb.WriteString("// compatible open source license.\n")
	sb.WriteByte('\n')
	sb.WriteString("// Code generated by cmd/osgen; DO NOT EDIT.\n")
	if f.BuildTag != "" {
		sb.WriteString("\n//go:build ")
		sb.WriteString(f.BuildTag)
		sb.WriteByte('\n')
	}
	sb.WriteString("\npackage ")
	sb.WriteString(f.Package)
	sb.WriteByte('\n')

	sorted := imps.Grouped()
	hasImports := len(sorted[0]) > 0 || len(sorted[1]) > 0 || len(sorted[2]) > 0
	if hasImports {
		sb.WriteString("\nimport (\n")
		first := true
		for _, group := range sorted {
			if len(group) == 0 {
				continue
			}
			if !first {
				sb.WriteByte('\n')
			}
			first = false
			for _, imp := range group {
				if imp.Alias != "" {
					fmt.Fprintf(&sb, "\t%s %q\n", imp.Alias, imp.Path)
				} else {
					fmt.Fprintf(&sb, "\t%q\n", imp.Path)
				}
			}
		}
		sb.WriteString(")\n")
	}

	for _, body := range bodies {
		sb.WriteByte('\n')
		sb.WriteString(body)
		if !strings.HasSuffix(body, "\n") {
			sb.WriteByte('\n')
		}
	}

	formatted, err := format.Source([]byte(sb.String()))
	if err != nil {
		return nil, fmt.Errorf("formatting %s: %w\n%s", f.FilePath, err, sb.String())
	}
	return formatted, nil
}
