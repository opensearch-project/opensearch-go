type renderData struct {
	Pkg      string
	Exported bool
	Builders []builder
}

func (d renderData) MethodName() string {
	if d.Exported {
		return "Build"
	}
	return "build"
}

func (d renderData) HasRequired() bool {
	for _, b := range d.Builders {
		for _, f := range b.Fields {
			if f.Required {
				return true
			}
		}
	}
	return false
}

func commentWrap(s string) string {
	s = strings.TrimSpace(s)
	if s == "" {
		return ""
	}
	var sb strings.Builder
	sb.WriteString("//\n")
	for _, line := range strings.Split(s, "\n") {
		line = strings.TrimRight(line, " \t")
		if line == "" {
			sb.WriteString("//\n")
		} else {
			sb.WriteString("// ")
			sb.WriteString(line)
			sb.WriteByte('\n')
		}
	}
	return strings.TrimRight(sb.String(), "\n")
}

// commentLineWidth is the maximum total line length for generated Go doc
// comments, including the "// " prefix (3 chars).
const commentLineWidth = 72

// commentContentWidth is the usable character budget per line after the
// "// " prefix.
const commentContentWidth = commentLineWidth - len("// ")

// wrapLine takes a single logical comment line (without "// " prefix) and
// word-wraps it so no output line exceeds commentLineWidth. Each output
// line is prefixed with "// ". The result includes a leading "//\n" as a
// paragraph separator for use within Go doc comment blocks.
func wrapLine(text string) string {
	text = strings.TrimSpace(text)
	if text == "" {
		return ""
	}

	words := strings.Fields(text)
	var sb strings.Builder
	sb.WriteString("//\n")

	lineLen := 0
	for i, w := range words {
		if i == 0 {
			sb.WriteString("// ")
			sb.WriteString(w)
			lineLen = len(w)
			continue
		}
		if lineLen+1+len(w) > commentContentWidth {
			sb.WriteString("\n// ")
			sb.WriteString(w)
			lineLen = len(w)
		} else {
			sb.WriteByte(' ')
			sb.WriteString(w)
			lineLen += 1 + len(w)
		}
	}
	return sb.String()
}


// deprecComment renders a deprecation or availability annotation with its
// message. The prefix is the structured part (e.g. "Deprecated: >= 3.0.0."
// or "Availability: >= 2.0.0; <= 3.0.0.").
//
// If the message contains no newlines, the prefix and message are combined
// and word-wrapped as a single paragraph. If the message contains newlines,
// the prefix is emitted on its own line and the message is rendered as an
// indented preformatted block (preserving go doc formatting semantics).
func deprecComment(prefix, msg string) string {
	prefix = strings.TrimSpace(prefix)
	msg = strings.TrimSpace(msg)

	if msg == "" {
		return wrapLine(prefix)
	}

	if !strings.Contains(msg, "\n") {
		return wrapLine(prefix + " " + msg)
	}

	// Multi-line message: emit prefix as a paragraph, then the message
	// as an indented block so go doc renders it preformatted.
	var sb strings.Builder
	sb.WriteString("//\n")
	sb.WriteString("// ")
	sb.WriteString(prefix)
	sb.WriteString("\n//\n")
	for _, line := range strings.Split(msg, "\n") {
		line = strings.TrimRight(line, " \t")
		if line == "" {
			sb.WriteString("//\n")
		} else {
			sb.WriteString("//\t")
			sb.WriteString(line)
			sb.WriteByte('\n')
		}
	}
	return strings.TrimRight(sb.String(), "\n")
}

func normalizeSemver(v string) string {
	switch strings.Count(v, ".") {
	case 0:
		return v + ".0.0"
	case 1:
		return v + ".0"
	default:
		return v
	}
}

func render(builders []builder, pkg string, exported bool) (string, error) {
	var sb strings.Builder
	if err := codeTmpl.Execute(&sb, renderData{Pkg: pkg, Exported: exported, Builders: builders}); err != nil {
		return "", err
	}

	formatted, err := format.Source([]byte(sb.String()))
	if err != nil {
		lineNum := 0
		fmt.Sscanf(err.Error(), "%d:", &lineNum)
		if lineNum > 0 {
			return "", fmt.Errorf("%w\n%s", err, extractLines(sb.String(), lineNum-5, lineNum+10))
		}
		return "", err
	}
	return string(formatted), nil
}

func extractLines(s string, from, to int) string {
	lines := strings.Split(s, "\n")
	if from < 0 {
		from = 0
	}
	if to > len(lines) {
		to = len(lines)
	}
	var out strings.Builder
	for i := from; i < to; i++ {
		fmt.Fprintf(&out, "%d: %s\n", i+1, lines[i])
	}
	return out.String()
}
