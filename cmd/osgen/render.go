type renderData struct {
	Pkg      string
	Exported bool
	Builders []builder
}

func (d renderData) MethodName() string {
	if d.Exported {
		return "Build"
	}
	return "build"
}

func (d renderData) HasRequired() bool {
	for _, b := range d.Builders {
		for _, f := range b.Fields {
			if f.Required {
				return true
			}
		}
	}
	return false
}

// render executes the template and runs gofmt on the result.
func render(builders []builder, pkg string, exported bool) (string, error) {
	var sb strings.Builder
	if err := codeTmpl.Execute(&sb, renderData{Pkg: pkg, Exported: exported, Builders: builders}); err != nil {
		return "", err
	}

	formatted, err := format.Source([]byte(sb.String()))
	if err != nil {
		lineNum := 0
		fmt.Sscanf(err.Error(), "%d:", &lineNum)
		if lineNum > 0 {
			return "", fmt.Errorf("%w\n%s", err, extractLines(sb.String(), lineNum-5, lineNum+10))
		}
		return "", err
	}
	return string(formatted), nil
}

func extractLines(s string, from, to int) string {
	lines := strings.Split(s, "\n")
	if from < 0 {
		from = 0
	}
	if to > len(lines) {
		to = len(lines)
	}
	var out strings.Builder
	for i := from; i < to; i++ {
		fmt.Fprintf(&out, "%d: %s\n", i+1, lines[i])
	}
	return out.String()
}
