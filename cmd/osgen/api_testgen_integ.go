// integTestConfig holds all data needed by the integration test template.
type integTestConfig struct {
	Pkg                 string
	PkgName             string
	CorePkgName         string
	ImportPath          string
	ModulePath          string
	CorePkg             string
	TypePrefix          string
	VersionAdded        string
	IndexPrefix         string
	FixtureCode         string
	SkipReason          string
	CallExpr            string
	FailCallExpr        string
	NeedIndex           bool
	NeedOsClient        bool
	NeedImportPkg       bool
	NeedImportCore      bool
	NeedImportStrings   bool
	NeedImportTestutil  bool
	NeedImportOsapitest bool
	IsPlugin            bool
	IsNoBody            bool
}

// renderIntegTest generates an integration test file for one operation.
func renderIntegTest(op apiOperation, pkg, importPkg, corePkg string) (string, error) {
	isPlugin := pkg != corePkg
	cfg := classifyOperation(op, pkg, corePkg, isPlugin)
	cfg.Pkg = pkg
	cfg.PkgName = pkg
	cfg.CorePkgName = corePkg
	cfg.ImportPath = importPkg
	cfg.ModulePath = modulePath
	cfg.CorePkg = corePkg
	cfg.IsPlugin = isPlugin

	var sb strings.Builder
	if err := integTestTmpl.Execute(&sb, cfg); err != nil {
		return "", fmt.Errorf("executing integ test template for %s: %w", op.Group, err)
	}

	formatted, err := format.Source([]byte(sb.String()))
	if err != nil {
		return "", fmt.Errorf("formatting integ test for %s: %w\n%s", op.Group, err, sb.String())
	}
	return string(formatted), nil
}

// classifyOperation determines what fixtures and call expressions are needed.
func classifyOperation(op apiOperation, pkg, corePkg string, isPlugin bool) integTestConfig {
	cfg := integTestConfig{
		TypePrefix:   op.TypePrefix,
		VersionAdded: op.VersionAdded,
		IsNoBody:     op.IsNoBody,
	}

	if cfg.VersionAdded == "1.0.0" || cfg.VersionAdded == "1.0" {
		cfg.VersionAdded = ""
	}

	var callPrefix string
	if isPlugin {
		suffix := op.Group
		if idx := strings.IndexByte(suffix, '.'); idx >= 0 {
			suffix = suffix[idx+1:]
		}
		callPrefix = "client." + methodNameFromSuffix(suffix)
	} else {
		route := primaryRoute(op)
		callPrefix = "client."
		if route.FieldPath != "" {
			callPrefix += route.FieldPath + "."
		}
		callPrefix += route.MethodName
	}

	hasRequiredIndex := false
	hasRequiredID := false
	for _, f := range op.PathBuilder.Fields {
		if !f.Required {
			continue
		}
		switch f.Name {
		case "Index":
			hasRequiredIndex = true
		case "ID", "DocumentID":
			hasRequiredID = true
		}
	}

	isMutating := len(op.HTTPMethods) > 0 && (op.HTTPMethods[0] == http.MethodPost ||
		op.HTTPMethods[0] == http.MethodPut ||
		op.HTTPMethods[0] == http.MethodDelete ||
		op.HTTPMethods[0] == http.MethodPatch)

	needsBody := op.HasBody && len(op.HTTPMethods) > 0 && op.HTTPMethods[0] != http.MethodGet

	if skipReason := manualSetupReason(op.Group); skipReason != "" {
		cfg.SkipReason = skipReason
	}

	cfg.IndexPrefix = "test-" + kebabCase(op.TypePrefix)
	if hasRequiredIndex {
		cfg.NeedIndex = true
	}
	if hasRequiredID {
		cfg.NeedIndex = true
		cfg.FixtureCode = buildDocFixture(corePkg, isPlugin)
	} else if hasRequiredIndex && needsExistingIndex(op) {
		cfg.FixtureCode = buildIndexFixture(corePkg, isPlugin)
	}

	cfg.CallExpr = buildCallExpr(callPrefix, op, pkg, hasRequiredIndex, hasRequiredID, needsBody, isMutating, false)
	cfg.FailCallExpr = buildCallExpr(callPrefix, op, pkg, hasRequiredIndex, hasRequiredID, needsBody, isMutating, true)

	cfg.NeedImportPkg = cfg.NeedIndex || cfg.FixtureCode != "" || !op.IsPointerReq || hasRequiredIndex || hasRequiredID || needsBody
	cfg.NeedImportCore = isPlugin && cfg.NeedIndex
	cfg.NeedOsClient = isPlugin && (cfg.NeedIndex || cfg.VersionAdded != "")
	cfg.NeedImportTestutil = !isPlugin || cfg.NeedOsClient || !op.IsNoBody || cfg.NeedIndex
	cfg.NeedImportOsapitest = !op.IsNoBody
	cfg.NeedImportStrings = needsBody || hasRequiredID

	return cfg
}

// buildCallExpr constructs the Go expression for calling the API method.
func buildCallExpr(callPrefix string, op apiOperation, pkg string, hasRequiredIndex, hasRequiredID, needsBody, isMutating, isFailing bool) string {
	prefix := callPrefix
	if isFailing {
		prefix = strings.Replace(callPrefix, "client.", "failingClient.", 1)
	}

	if op.IsPointerReq {
		if !hasRequiredIndex && !hasRequiredID && !needsBody {
			return prefix + "(t.Context(), nil)"
		}
		return prefix + "(t.Context(), &" + buildReqLiteral(op, pkg, hasRequiredIndex, hasRequiredID, needsBody, isMutating) + ")"
	}
	return prefix + "(t.Context(), " + buildReqLiteral(op, pkg, hasRequiredIndex, hasRequiredID, needsBody, isMutating) + ")"
}

// buildReqLiteral constructs the Req struct literal.
func buildReqLiteral(op apiOperation, pkg string, hasRequiredIndex, hasRequiredID, needsBody, isMutating bool) string {
	var fields []string

	for _, f := range op.PathBuilder.Fields {
		if !f.Required {
			continue
		}
		switch f.Name {
		case "Index":
			if f.IsList {
				fields = append(fields, "Index: []string{index}")
			} else {
				fields = append(fields, "Index: index")
			}
		case "ID", "DocumentID":
			fields = append(fields, fmt.Sprintf("%s: %q", f.Name, "test-doc-1"))
		default:
			if f.IsList {
				fields = append(fields, fmt.Sprintf("%s: []string{%q}", f.Name, "test-value"))
			} else {
				fields = append(fields, fmt.Sprintf("%s: %q", f.Name, "test-value"))
			}
		}
	}

	if needsBody {
		fields = append(fields, `Body: strings.NewReader("{}")`)
	}

	if isMutating && hasRequiredIndex && !hasRequiredID && hasRefreshParam(op) {
		fields = append(fields, fmt.Sprintf("Params: %s.%sParams{Refresh: %q}", pkg, op.TypePrefix, "true"))
	}

	return fmt.Sprintf("%s.%sReq{%s}", pkg, op.TypePrefix, strings.Join(fields, ", "))
}

// hasRefreshParam returns true if the operation's query params include Refresh.
func hasRefreshParam(op apiOperation) bool {
	for _, p := range op.QueryParams {
		if p.GoName == "Refresh" {
			return true
		}
	}
	return false
}

// needsExistingIndex returns true if an operation requires the index to already
// exist (as opposed to creating it).
func needsExistingIndex(op apiOperation) bool {
	if strings.Contains(op.Group, "create") {
		return false
	}
	if len(op.HTTPMethods) == 0 {
		return true
	}
	switch op.HTTPMethods[0] {
	case http.MethodGet, http.MethodHead, http.MethodDelete, http.MethodPost:
		return true
	case http.MethodPut:
		return !strings.Contains(op.Group, "create")
	}
	return true
}

// buildIndexFixture creates the fixture code for operations needing an existing index.
func buildIndexFixture(corePkg string, isPlugin bool) string {
	c := "client"
	if isPlugin {
		c = "osClient"
	}
	return fmt.Sprintf(`_, err = %s.Indices.Create(t.Context(), %s.IndicesCreateReq{Index: index})
	require.NoError(t, err)`, c, corePkg)
}

// buildDocFixture creates the fixture code for operations needing a document.
func buildDocFixture(corePkg string, isPlugin bool) string {
	c := "client"
	if isPlugin {
		c = "osClient"
	}
	return fmt.Sprintf(`_, err = %s.Indices.Create(t.Context(), %s.IndicesCreateReq{Index: index})
	require.NoError(t, err)

	_, err = %s.Index(t.Context(), %s.IndexReq{
		Index:  index,
		ID:     "test-doc-1",
		Body:   strings.NewReader(`+"`"+`{"title":"fixture"}`+"`"+`),
		Params: %s.IndexParams{Refresh: "true"},
	})
	require.NoError(t, err)`, c, corePkg, c, corePkg, corePkg)
}

// primaryRoute returns the first non-deprecated dispatch route.
func primaryRoute(op apiOperation) struct {
	FieldPath  string
	MethodName string
} {
	for _, route := range op.DispatchRoutes {
		if route.Deprecated {
			continue
		}
		fieldPath := ""
		if !route.TopLevel {
			fieldPath = resolveFieldPath(route.ReceiverType)
		}
		return struct {
			FieldPath  string
			MethodName string
		}{FieldPath: fieldPath, MethodName: route.MethodName}
	}
	return struct {
		FieldPath  string
		MethodName string
	}{MethodName: op.TypePrefix}
}

// manualSetupReason returns a skip reason for operations that need complex
// cluster state not achievable in an automated test.
func manualSetupReason(group string) string {
	switch {
	case strings.HasPrefix(group, "dangling_indices"):
		return "requires dangling index state from node failure"
	case strings.HasPrefix(group, "snapshot"):
		return "requires snapshot repository configuration"
	case group == "reindex":
		return "requires source index with documents"
	case group == "reindex_rethrottle":
		return "requires active reindex task"
	case strings.HasPrefix(group, "tasks"):
		return "requires active long-running task"
	case group == "bulk" || group == "bulk_stream":
		return "requires NDJSON body with action metadata"
	case group == "msearch" || group == "msearch_template":
		return "requires NDJSON multi-search body"
	case group == "scroll.get" || group == "scroll.delete":
		return "requires active scroll context"
	case group == "clear_scroll":
		return "requires active scroll context"
	case strings.HasPrefix(group, "pit."):
		return "requires point-in-time context"
	case group == "rank_eval":
		return "requires rank evaluation body with rated documents"
	case group == "scripts_painless.execute":
		return "requires painless script body"
	case group == "render_search_template":
		return "requires search template body"
	case group == "field_caps":
		return "requires index with mapped fields"
	case group == "termvectors" || group == "mtermvectors":
		return "requires index with term vectors enabled"
	}
	return ""
}

// kebabCase converts PascalCase to kebab-case for test index prefixes.
func kebabCase(s string) string {
	var result strings.Builder
	for i, r := range s {
		if r >= 'A' && r <= 'Z' {
			if i > 0 {
				result.WriteByte('-')
			}
			result.WriteRune(r + 32)
		} else {
			result.WriteRune(r)
		}
	}
	return result.String()
}
