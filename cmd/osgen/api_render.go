// NestedFields returns sub-clients nested inside the given parent type.
func (d clientsTemplateData) NestedFields(parent string) []subClientInfo {
	var result []subClientInfo
	for _, sc := range d.hierarchy {
		if sc.Parent == parent && sc.Parent != "Client" {
			result = append(result, sc)
		}
	}
	return result
}

// renderClientsFile generates the clients_gen.go file containing Client struct,
// sub-client types, clientInit, Inspect alias, and noBody sentinel.
func renderClientsFile(pkg string) (string, error) {
	var topLevel []subClientInfo
	var allClients []subClientInfo
	var initStmts []string

	for _, sc := range subClientHierarchy {
		allClients = append(allClients, sc)
		if sc.Parent == "Client" {
			topLevel = append(topLevel, sc)
		}
	}

	for _, sc := range subClientHierarchy {
		if sc.Parent == "Client" {
			initStmts = append(initStmts, fmt.Sprintf("client.%s = %s{apiClient: client}", sc.FieldName, sc.TypeName))
		}
	}
	// Nested sub-clients need their parent set up first, so emit them after.
	for _, sc := range subClientHierarchy {
		if sc.Parent != "Client" {
			parentField := parentFieldName(sc.Parent)
			initStmts = append(initStmts, fmt.Sprintf("client.%s.%s = %s{apiClient: client}", parentField, sc.FieldName, sc.TypeName))
		}
	}

	data := clientsTemplateData{
		Pkg:             pkg,
		TopLevelClients: topLevel,
		SubClients:      allClients,
		InitStatements:  initStmts,
		hierarchy:       subClientHierarchy,
	}

	var sb strings.Builder
	if err := clientsTmpl.Execute(&sb, data); err != nil {
		return "", err
	}

	formatted, err := format.Source([]byte(sb.String()))
	if err != nil {
		return "", fmt.Errorf("formatting clients_gen.go: %w\n%s", err, sb.String())
	}
	return string(formatted), nil
}

// parentFieldName returns the exported field name on Client for a given parent type.
func parentFieldName(parentType string) string {
	for _, sc := range subClientHierarchy {
		if sc.TypeName == parentType {
			return sc.FieldName
		}
	}
	return parentType
}

// fieldHasAnnotation returns true if the field has any doc comment or version note.
func fieldHasAnnotation(f goField) bool {
	return f.Comment != "" || f.VersionAdded != "" || f.VersionDeprecated != ""
}

// fieldNeedsSep returns true if a blank line should precede field[i]. A blank
// line is inserted whenever either the current or previous field carries a
// comment or version annotation. Consecutive uncommented fields are grouped.
func fieldNeedsSep(fields []goField, i int) bool {
	if i == 0 {
		return false
	}
	return fieldHasAnnotation(fields[i-1]) || fieldHasAnnotation(fields[i])
}

// wrapField wraps a description for use as a struct field doc comment.
// Output is tab-indented with "// " prefix on each line. No leading
// separator is included; the template handles blank-line separation.
func wrapField(text string) string {
	text = strings.TrimSpace(text)
	if text == "" {
		return ""
	}

	const maxWidth = 72 // content width after "\t// "
	words := strings.Fields(text)
	var sb strings.Builder

	lineLen := 0
	for i, w := range words {
		if i == 0 {
			sb.WriteString("// ")
			sb.WriteString(w)
			lineLen = len(w)
			continue
		}
		if lineLen+1+len(w) > maxWidth {
			sb.WriteString("\n\t// ")
			sb.WriteString(w)
			lineLen = len(w)
		} else {
			sb.WriteByte(' ')
			sb.WriteString(w)
			lineLen += 1 + len(w)
		}
	}
	return sb.String()
}

// availabilityNote builds the version/deprecation annotation text from the
// raw version fields. Returns empty string if no annotation is needed.
//
// Examples:
//
//	availabilityNote("1.0", "", "")          => "Available: >= 1.0.0."
//	availabilityNote("", "2.0", "Use X.")    => "Deprecated: since 2.0.0. Use X."
//	availabilityNote("1.0", "2.0", "Use X.") => "Deprecated: since 2.0.0. Available >= 1.0.0. Use X."
func availabilityNote(versionAdded, versionDeprecated, deprecMsg string) string {
	versionAdded = strings.TrimSpace(versionAdded)
	versionDeprecated = strings.TrimSpace(versionDeprecated)
	deprecMsg = strings.TrimSpace(deprecMsg)

	if versionAdded == "" && versionDeprecated == "" {
		return ""
	}

	var sb strings.Builder

	if versionDeprecated != "" {
		sb.WriteString("Deprecated: since ")
		sb.WriteString(normalizeSemver(versionDeprecated))
		sb.WriteString(".")
		if versionAdded != "" {
			sb.WriteString(" Available >= ")
			sb.WriteString(normalizeSemver(versionAdded))
			sb.WriteString(".")
		}
		if deprecMsg != "" {
			sb.WriteString(" ")
			sb.WriteString(deprecMsg)
		}
	} else {
		sb.WriteString("Available: >= ")
		sb.WriteString(normalizeSemver(versionAdded))
		sb.WriteString(".")
	}

	return sb.String()
}
