// renderAPIFile executes the apiTmpl template for one operation and returns
// gofmt'd source code containing the Req, Params, and Resp types.
//
// The registry is used to resolve cross-package type references. When a field
// references a type whose import path differs from the file being rendered,
// the type name is qualified with the appropriate package prefix and the
// import is added.
func renderAPIFile(op apiOperation, pkg string, registry *typeRegistry) (string, error) {
	hasDuration := false
	hasList := true // FilterPath always uses strings.Join.
	hasInt := false
	hasJSON := false
	for _, p := range op.QueryParams {
		if p.IsDuration {
			hasDuration = true
		}
		if p.IsList {
			hasList = true
		}
		if p.IsInt {
			hasInt = true
		}
	}
	if usesJSONRaw(op.RespFields, op.SiblingTypes) {
		hasJSON = true
	}

	// Separate union types from struct siblings.
	var unionSiblings []*goType
	var structSiblings []*goType
	for _, st := range op.SiblingTypes {
		if st.IsUnion {
			unionSiblings = append(unionSiblings, st)
		} else {
			structSiblings = append(structSiblings, st)
		}
	}
	op.SiblingTypes = structSiblings

	// Union types need encoding/json and fmt.
	if len(unionSiblings) > 0 {
		hasJSON = true
	}

	filePkg := importPathForPkg(op.Group, registry.CorePkg)
	extraImports := make(map[string]struct{})
	op = qualifyCrossPackageTypes(op, filePkg, registry, extraImports)

	if len(unionSiblings) > 0 {
		needImportFmt := false
		for _, u := range unionSiblings {
			if !u.IsLazy {
				needImportFmt = true
				break
			}
		}
		if needImportFmt {
			extraImports["fmt"] = struct{}{}
		}
	}

	data := struct {
		apiOperation
		Pkg          string
		HasDispatch  bool
		HasDuration  bool
		HasList      bool
		HasInt       bool
		HasJSON      bool
		ExtraImports []string
	}{
		apiOperation: op,
		Pkg:          pkg,
		HasDispatch:  len(op.DispatchRoutes) > 0,
		HasDuration:  hasDuration,
		HasList:      hasList,
		HasInt:       hasInt,
		HasJSON:      hasJSON,
	}
	for imp := range extraImports {
		data.ExtraImports = append(data.ExtraImports, imp)
	}
	sort.Strings(data.ExtraImports)

	var sb strings.Builder
	if err := apiTmpl.Execute(&sb, data); err != nil {
		return "", err
	}

	// Render operation-specific union types into the same file.
	if len(unionSiblings) > 0 {
		unionData := struct {
			Types []*goType
		}{Types: unionSiblings}
		if err := unionTmpl.Execute(&sb, unionData); err != nil {
			return "", err
		}
	}

	formatted, err := format.Source([]byte(sb.String()))
	if err != nil {
		return "", err
	}
	return string(formatted), nil
}

// qualifyCrossPackageTypes returns a copy of op with type references qualified
// by their package name when they belong to a different import path than filePkg.
// Any required imports are collected in extraImports.
func qualifyCrossPackageTypes(op apiOperation, filePkg string, registry *typeRegistry, extraImports map[string]struct{}) apiOperation {
	op.RespFields = qualifyFields(op.RespFields, filePkg, registry, extraImports)
	if len(op.SiblingTypes) > 0 {
		siblings := make([]*goType, 0, len(op.SiblingTypes))
		for _, st := range op.SiblingTypes {
			if st.Pkg != "" && st.Pkg != filePkg {
				continue
			}
			clone := *st
			clone.Fields = qualifyFields(clone.Fields, filePkg, registry, extraImports)
			siblings = append(siblings, &clone)
		}
		op.SiblingTypes = siblings
	}
	return op
}

// qualifyFields returns a copy of fields with cross-package type names qualified.
func qualifyFields(fields []goField, filePkg string, registry *typeRegistry, extraImports map[string]struct{}) []goField {
	result := make([]goField, len(fields))
	copy(result, fields)
	for i := range result {
		result[i].GoType = qualifyTypeName(result[i].GoType, filePkg, registry, extraImports)
	}
	return result
}

// qualifyTypeName prefixes a type name with its package qualifier when it
// belongs to a different import path. Handles pointer, slice, and map wrappers.
func qualifyTypeName(goType, filePkg string, registry *typeRegistry, extraImports map[string]struct{}) string {
	if strings.HasPrefix(goType, "*") {
		inner := qualifyTypeName(goType[1:], filePkg, registry, extraImports)
		return "*" + inner
	}
	if strings.HasPrefix(goType, "[]") {
		inner := qualifyTypeName(goType[2:], filePkg, registry, extraImports)
		return "[]" + inner
	}
	if strings.HasPrefix(goType, "map[") {
		closeBracket := strings.Index(goType, "]")
		if closeBracket >= 0 {
			key := goType[:closeBracket+1]
			val := qualifyTypeName(goType[closeBracket+1:], filePkg, registry, extraImports)
			return key + val
		}
	}
	t, ok := registry.lookupByName(goType)
	if !ok || t.Pkg == "" || t.Pkg == filePkg {
		return goType
	}
	pkgName := importPathBase(t.Pkg)
	extraImports[t.Pkg] = struct{}{}
	return pkgName + "." + goType
}

// importPathBase returns the last segment of an import path (the package name).
func importPathBase(imp string) string {
	if idx := strings.LastIndexByte(imp, '/'); idx >= 0 {
		return imp[idx+1:]
	}
	return imp
}

