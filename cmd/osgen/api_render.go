// renderAPIFile executes the apiTmpl template for one operation and returns
// gofmt'd source code containing the Req, Params, and Resp types.
func renderAPIFile(op apiOperation, pkg string) (string, error) {
	hasDuration := false
	hasList := true // FilterPath always uses strings.Join.
	hasInt := false
	for _, p := range op.QueryParams {
		if p.IsDuration {
			hasDuration = true
		}
		if p.IsList {
			hasList = true
		}
		if p.IsInt {
			hasInt = true
		}
	}

	data := struct {
		apiOperation
		Pkg         string
		HasDuration bool
		HasList     bool
		HasInt      bool
	}{
		apiOperation: op,
		Pkg:          pkg,
		HasDuration:  hasDuration,
		HasList:      hasList,
		HasInt:       hasInt,
	}

	var sb strings.Builder
	if err := apiTmpl.Execute(&sb, data); err != nil {
		return "", err
	}

	formatted, err := format.Source([]byte(sb.String()))
	if err != nil {
		return "", err
	}
	return string(formatted), nil
}

// renderCompatFile generates the compat_gen.go file for a plugin package,
// which re-exports shared types (like Inspect) from internal/apiutil.
func renderCompatFile(pkg string) (string, error) {
	var sb strings.Builder
	if err := compatTmpl.Execute(&sb, struct{ Pkg string }{Pkg: pkg}); err != nil {
		return "", err
	}

	formatted, err := format.Source([]byte(sb.String()))
	if err != nil {
		return "", err
	}
	return string(formatted), nil
}
