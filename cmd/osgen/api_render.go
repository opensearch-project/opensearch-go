// usesJSONRaw returns true if any response field or sibling type field
// uses json.RawMessage, requiring the "encoding/json" import.
func usesJSONRaw(respFields []goField, siblings []*goType) bool {
	for _, f := range respFields {
		if strings.Contains(f.GoType, "json.RawMessage") {
			return true
		}
	}
	for _, t := range siblings {
		for _, f := range t.Fields {
			if strings.Contains(f.GoType, "json.RawMessage") {
				return true
			}
		}
	}
	return false
}

// renderSharedTypesFile generates the types_gen.go file containing shared
// types (from _common___ and group._common___ schemas).
func renderSharedTypesFile(types []*goType, pkg string) (string, error) {
	if len(types) == 0 {
		return "", nil
	}

	hasJSON := false
	for _, t := range types {
		for _, f := range t.Fields {
			if strings.Contains(f.GoType, "json.RawMessage") {
				hasJSON = true
				break
			}
		}
		if hasJSON {
			break
		}
	}

	data := struct {
		Pkg     string
		Types   []*goType
		HasJSON bool
	}{
		Pkg:     pkg,
		Types:   types,
		HasJSON: hasJSON,
	}

	var sb strings.Builder
	if err := sharedTypesTmpl.Execute(&sb, data); err != nil {
		return "", err
	}

	formatted, err := format.Source([]byte(sb.String()))
	if err != nil {
		return "", err
	}
	return string(formatted), nil
}

// clientsTemplateData holds the data for the clients_gen.go template.
