package main

import (
	"fmt"
	"go/format"
	"strings"
	"text/template"
)

var paramsTmpl = template.Must(template.New("params").Funcs(template.FuncMap{
	"semver":        normalizeSemver,
	"deprecComment": deprecComment,
	"printf":        fmt.Sprintf,
}).Parse(`// Code generated by cmd/osgen; DO NOT EDIT.

package {{.Pkg}}

import (
{{- if .HasDuration}}
	"time"
{{- end}}
{{- if .HasList}}
	"strings"
{{- end}}
{{- if .HasInt}}
	"strconv"
{{- end}}
)

// {{.TypePrefix}}Params represents query parameters for the {{.TypePrefix}}Req.
{{- if and .VersionAdded .Deprecated}}
{{deprecComment (printf "Availability: >= %s; <= %s." (semver .VersionAdded) (semver .VersionDeprecated)) .DeprecationMsg}}
{{- else if .VersionAdded}}
//
// Available: >= {{semver .VersionAdded}}
{{- else if .Deprecated}}
{{deprecComment (printf "Deprecated: >= %s." (semver .VersionDeprecated)) .DeprecationMsg}}
{{- end}}
type {{.TypePrefix}}Params struct {
{{- range .QueryParams}}
{{- if .Deprecated}}
	// Deprecated: >= {{semver .VersionDeprecated}}.
{{- end}}
	{{.GoName}} {{.GoType}}
{{- end}}

	// Pretty enables indented JSON responses for debugging readability.
	Pretty     bool
	// Human enables human-friendly values in responses (e.g. "1.5gb" instead of bytes).
	Human      bool
	// ErrorTrace includes the Java stack trace of errors in the response.
	ErrorTrace bool
	// FilterPath restricts the response to the specified JSON paths.
	FilterPath []string
}

func (r {{.TypePrefix}}Params) get() map[string]string {
	params := make(map[string]string)
{{range .QueryParams}}
{{- if .IsDuration}}
	if r.{{.GoName}} != 0 {
		params["{{.ParamName}}"] = formatDuration(r.{{.GoName}})
	}
{{- else if .IsBool}}
	if r.{{.GoName}} {
		params["{{.ParamName}}"] = "true"
	}
{{- else if .IsList}}
	if len(r.{{.GoName}}) > 0 {
		params["{{.ParamName}}"] = strings.Join(r.{{.GoName}}, ",")
	}
{{- else if .IsInt}}
	if r.{{.GoName}} != 0 {
		params["{{.ParamName}}"] = strconv.Itoa(r.{{.GoName}})
	}
{{- else}}
	if r.{{.GoName}} != "" {
		params["{{.ParamName}}"] = r.{{.GoName}}
	}
{{- end}}
{{end}}
	if r.Pretty {
		params["pretty"] = "true"
	}

	if r.Human {
		params["human"] = "true"
	}

	if r.ErrorTrace {
		params["error_trace"] = "true"
	}

	if len(r.FilterPath) > 0 {
		params["filter_path"] = strings.Join(r.FilterPath, ",")
	}

	return params
}
`))

// renderParamsFile executes the paramsTmpl template for one operation and
// returns gofmt'd source code for the Params file.
func renderParamsFile(op apiOperation, pkg string) (string, error) {
	hasDuration := false
	hasList := false
	hasInt := false
	for _, p := range op.QueryParams {
		if p.IsDuration {
			hasDuration = true
		}
		if p.IsList {
			hasList = true
		}
		if p.IsInt {
			hasInt = true
		}
	}
	// FilterPath always uses strings.Join.
	hasList = true

	data := struct {
		apiOperation
		Pkg         string
		HasDuration bool
		HasList     bool
		HasInt      bool
	}{
		apiOperation: op,
		Pkg:          pkg,
		HasDuration:  hasDuration,
		HasList:      hasList,
		HasInt:       hasInt,
	}

	var sb strings.Builder
	if err := paramsTmpl.Execute(&sb, data); err != nil {
		return "", err
	}

	formatted, err := format.Source([]byte(sb.String()))
	if err != nil {
		return "", err
	}
	return string(formatted), nil
}
